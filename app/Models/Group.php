    public static function definitionBySlug(string $slug): ?array
    {
        foreach (self::categoryDefinitions() as $definition) {
            if ($definition['slug'] === $slug) {
                return $definition;
            }
        }

        return null;
    }

    public static function definitionByTitle(string $title): ?array
    {
        $normalizedTitle = self::normalizeCategoryValue($title);

        foreach (self::categoryDefinitions() as $definition) {
            foreach ($definition['aliases'] as $alias) {
                if (self::normalizeCategoryValue($alias) === $normalizedTitle) {
                    return $definition;
                }
            }
        }

        return null;
    }

    public static function normalizeCategoryValue(string $value): string
    {
        $value = mb_strtolower(trim($value));
        $value = str_replace('ё', 'е', $value);

        return preg_replace('/[\s\-]+/u', '', $value);
    }

